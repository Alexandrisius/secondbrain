/**
 * –ü–æ–ª—É—á–∏—Ç—å API-–∫–ª—é—á –∏–∑ OS vault.
 *
 * –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç:
 * - string (–∫–ª—é—á) –µ—Å–ª–∏ –æ–Ω —Å–æ—Ö—Ä–∞–Ω—ë–Ω –∏ —É—Å–ø–µ—à–Ω–æ —Ä–∞—Å—à–∏—Ñ—Ä–æ–≤–∞–Ω
 * - null –µ—Å–ª–∏ –∫–ª—é—á –Ω–µ –Ω–∞–π–¥–µ–Ω / —à–∏—Ñ—Ä–æ–≤–∞–Ω–∏–µ –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–æ / –ø—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞
 *
 * –í–ê–ñ–ù–û:
 * - –ù–∏–∫–æ–≥–¥–∞ –Ω–µ –ª–æ–≥–∏—Ä—É–µ–º –Ω–∏ –∫–ª—é—á, –Ω–∏ —à–∏—Ñ—Ä—Ç–µ–∫—Å—Ç.
 */
ipcMain.handle('secure-api-key:get', async () => {
  try {
    if (!safeStorage.isEncryptionAvailable()) return null;

    const filePath = getSecureApiKeyFilePath();
    if (!fs.existsSync(filePath)) return null;

    // –ß–∏—Ç–∞–µ–º base64-—Å—Ç—Ä–æ–∫—É (UTF-8), –∑–∞—Ç–µ–º –¥–µ–∫–æ–¥–∏—Ä—É–µ–º –≤ Buffer
    const base64 = await fs.promises.readFile(filePath, 'utf8');
    const trimmed = String(base64 || '').trim();
    if (!trimmed) return null;

    const encryptedBuffer = Buffer.from(trimmed, 'base64');
    const decrypted = safeStorage.decryptString(encryptedBuffer);

    const key = typeof decrypted === 'string' ? decrypted : '';
    return key.trim().length > 0 ? key : null;
  } catch {
    return null;
  }
});

/**
 * –°–æ—Ö—Ä–∞–Ω–∏—Ç—å API-–∫–ª—é—á –≤ OS vault.
 *
 * –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç boolean:
 * - true –µ—Å–ª–∏ —Å–æ—Ö—Ä–∞–Ω–µ–Ω–æ
 * - false –µ—Å–ª–∏ –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–æ/–æ—à–∏–±–∫–∞/–Ω–µ–≤–∞–ª–∏–¥–Ω—ã–π –≤–≤–æ–¥
 *
 * –í–ê–ñ–ù–û:
 * - –ú—ã –Ω–µ ‚Äú–≤–∞–ª–∏–¥–∏—Ä—É–µ–º —Ñ–æ—Ä–º–∞—Ç‚Äù –∫–ª—é—á–∞, –ø–æ—Ç–æ–º—É —á—Ç–æ –ø—Ä–æ–≤–∞–π–¥–µ—Ä–æ–≤ –º–Ω–æ–≥–æ.
 * - –ú—ã –Ω–µ –æ–±—Ä–µ–∑–∞–µ–º –ø—Ä–æ–±–µ–ª—ã –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏: –∫–ª—é—á ‚Äî —ç—Ç–æ opaque string.
 */
ipcMain.handle('secure-api-key:set', async (event, key) => {
  try {
    if (typeof key !== 'string') return false;
    if (!safeStorage.isEncryptionAvailable()) return false;

    const filePath = getSecureApiKeyFilePath();

    // –°–æ–∑–¥–∞—ë–º –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é userData –Ω–∞ –≤—Å—è–∫–∏–π —Å–ª—É—á–∞–π (–æ–±—ã—á–Ω–æ Electron —É–∂–µ —Å–æ–∑–¥–∞—ë—Ç –µ—ë)
    await fs.promises.mkdir(path.dirname(filePath), { recursive: true });

    const encrypted = safeStorage.encryptString(key);
    const base64 = encrypted.toString('base64');

    // –ü–∏—à–µ–º —Ç–æ–ª—å–∫–æ —à–∏—Ñ—Ä—Ç–µ–∫—Å—Ç, –±–µ–∑ –∫–∞–∫–∏—Ö-–ª–∏–±–æ –º–µ—Ç–∞–¥–∞–Ω–Ω—ã—Ö.
    await fs.promises.writeFile(filePath, base64, 'utf8');
    return true;
  } catch {
    return false;
  }
});

/**
 * –£–¥–∞–ª–∏—Ç—å API-–∫–ª—é—á –∏–∑ OS vault (—É–¥–∞–ª—è–µ–º —Ñ–∞–π–ª —Å —à–∏—Ñ—Ä—Ç–µ–∫—Å—Ç–æ–º).
 *
 * –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç true, –µ—Å–ª–∏:
 * - —Ñ–∞–π–ª —É—Å–ø–µ—à–Ω–æ —É–¥–∞–ª—ë–Ω, –ò–õ–ò —Ñ–∞–π–ª–∞ –Ω–µ –±—ã–ª–æ (–∏–¥–µ–º–ø–æ—Ç–µ–Ω—Ç–Ω–æ—Å—Ç—å)
 */
ipcMain.handle('secure-api-key:delete', async () => {
  try {
    const filePath = getSecureApiKeyFilePath();
    if (!fs.existsSync(filePath)) return true;

    await fs.promises.unlink(filePath);
    return true;
  } catch {
    return false;
  }
});

